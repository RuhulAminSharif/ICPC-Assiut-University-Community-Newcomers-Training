#include<bits/stdc++.h>
using namespace std;
#define ll long long int
#define endl "\n"
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    while(true){
        ll x; cin >> x;
        if(x==1999){
            cout << "Correct" << endl;
            break;
        }else{
            cout << "Wrong" << endl;
        }
    }
    return 0;
}