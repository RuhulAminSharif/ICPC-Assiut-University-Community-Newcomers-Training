#include<bits/stdc++.h>
using namespace std;
#define ll long long int
#define endl "\n"
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    ll x, y; cin >> x >> y;
    cout << x << " + " << y << " = " << x+y << endl;
    cout << x << " * " << y << " = " << x*y << endl;
    cout << x << " - " << y << " = " << x-y << endl;
    return 0;
}